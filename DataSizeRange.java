import java.lang.*;

class DataSizeRange {
	public static void main(String[] args) {
		System.out.println("Int min value: " + Integer.MIN_VALUE);
		System.out.println("Int max value: " + Integer.MAX_VALUE);
		System.out.println("Int bytes: " + Integer.BYTES);

		System.out.println();

		System.out.println("Float min value: " + Float.MIN_VALUE);
		System.out.println("Float max value: " + Float.MAX_VALUE);
		System.out.println("Float bytes: " + Float.BYTES);
		
		System.out.println();

		System.out.println("Short min value: " + Short.MIN_VALUE);
		System.out.println("Short max value: " + Short.MAX_VALUE);
		System.out.println("Short bytes: " + Short.BYTES);

		System.out.println();

		System.out.println("Double min value: " + Double.MIN_VALUE);
		System.out.println("Double max value: " + Double.MAX_VALUE);
		System.out.println("Double bytes: " + Double.BYTES);

		System.out.println();
	
		System.out.println("Long min value: " + Long.MIN_VALUE);
		System.out.println("Long max value: " + Long.MAX_VALUE);
		System.out.println("Long bytes: " + Long.BYTES);

		System.out.println();

		System.out.println("Byte min value: " + Byte.MIN_VALUE);
		System.out.println("Byte max value: " + Byte.MAX_VALUE);
		System.out.println("Byte bytes: " + Byte.BYTES);

		System.out.println();

		System.out.println("Character min value: " + Integer.MIN_VALUE);
		System.out.println("Character max value: " + Integer.MAX_VALUE);
		System.out.println("Character bytes: " + Integer.BYTES);
	}
}